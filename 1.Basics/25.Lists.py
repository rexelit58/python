names=['kavi','arasan','bob','mash','sarah']
print(names)
#pointing the element directly
print(names[3])
#pointing the last element directly
print(names[-1])
#pointing the start index and end index
print(names[2:4])
#pointing the start index to end
print(names[2:])
#iterating all the index
print(names[:])
#overriding all the value at runtime
names[0]="kaviking"
print(names)

