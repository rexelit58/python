#looping  through the string
for item in 'Python':
    print(item)
#looping  through the list
for item in ['kavi','arasan','king']:
    print(item)
# looping  through the number
for item in [1,2,3,4]:
    print(item)
# looping the  dynamic numbers using range function
for item in range(10):
    print(item)
for item in range(5,10):
    print(item)
for item in range(5,10,2):
    print(item)